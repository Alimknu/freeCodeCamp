-- 1. DATABASE SETUP
DROP DATABASE IF EXISTS bikes;
CREATE DATABASE bikes;
\connect bikes

-- 2. TABLE CREATION
CREATE TABLE public.bikes (
    bike_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type VARCHAR(50) NOT NULL,
    size INTEGER NOT NULL,
    available BOOLEAN DEFAULT true NOT NULL
);

CREATE TABLE public.customers (
    customer_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    phone VARCHAR(15) NOT NULL UNIQUE,
    name VARCHAR(40) NOT NULL
);

CREATE TABLE public.rentals (
    rental_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id INTEGER NOT NULL REFERENCES public.customers(customer_id),
    bike_id INTEGER NOT NULL REFERENCES public.bikes(bike_id),
    date_rented DATE DEFAULT NOW() NOT NULL,
    date_returned DATE
);

